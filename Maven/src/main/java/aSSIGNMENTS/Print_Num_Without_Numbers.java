package aSSIGNMENTS;

public class Print_Num_Without_Numbers {

	public static void main(String[] args) {

		//ascii value for e=100;
		
		//**************1 approach*****************************88
		int one='e'/'e';
		
		for(int i=one;i<'e';i++)
		{
			System.out.print(i+" ");
		}
		
		//*****************2nd*********************************************
		
		String s="SowmyaArun";
		int size=s.length();    	//10
		int One=size/size;			//10%10==1
		int hundred=size*size;		//10*10==100
		
		System.out.println("\n***********************************************");
		for(int i=One;i<=hundred;i++)
		{
			System.out.print(i+" ");
		}

	}

}
